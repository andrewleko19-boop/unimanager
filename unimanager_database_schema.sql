-- groups: members can read; only owner/admin mutate via RPCs (see Section 4).
drop policy if exists grp_select_member on public.groups;
create policy grp_select_member on public.groups
  for select to authenticated using (is_group_member(id));

-- group_members: a member can see the roster of their groups.
drop policy if exists gm_select_member on public.group_members;
create policy gm_select_member on public.group_members
  for select to authenticated using (is_group_member(group_id));

-- [FIX 2] group_members: a user may remove THEMSELVES (leaveGroup does a direct
-- delete). Without this, RLS denied the delete silently (0 rows, no error) and
-- the user stayed a member server-side. Adding/kicking still go through the
-- SECURITY DEFINER RPCs, so this self-delete is the only direct write allowed.
drop policy if exists gm_delete_self on public.group_members;
create policy gm_delete_self on public.group_members
  for delete to authenticated using (user_id = auth.uid());

-- Shared schedule / exams: members read; admins write.
do $$
declare t text;
begin
  foreach t in array array['group_shared_schedule','group_shared_exams']
  loop
    execute format('drop policy if exists %I on public.%I', t||'_sel', t);
    execute format('create policy %I on public.%I for select to authenticated using (is_group_member(group_id))', t||'_sel', t);
    execute format('drop policy if exists %I on public.%I', t||'_write', t);
    execute format('create policy %I on public.%I for all to authenticated using (is_group_admin(group_id)) with check (is_group_admin(group_id))', t||'_write', t);
  end loop;
end $$;

-- [FIX 1] group_subject_notes: collaborative notes — ANY member reads AND
-- writes (the app's upsertGroupNote is documented "any member"). Previously
-- this table shared the admin-only write policy with schedule/exams above,
-- which made every non-admin note save fail the WITH CHECK.
drop policy if exists group_subject_notes_sel on public.group_subject_notes;
create policy group_subject_notes_sel on public.group_subject_notes
  for select to authenticated using (is_group_member(group_id));

drop policy if exists group_subject_notes_write on public.group_subject_notes;
create policy group_subject_notes_write on public.group_subject_notes
  for all to authenticated
  using (is_group_member(group_id)) with check (is_group_member(group_id));

-- group_messages: members read; sender inserts own (honouring chat_mode);
-- sender (or admin) edits/deletes.
drop policy if exists msg_select_member on public.group_messages;
create policy msg_select_member on public.group_messages
  for select to authenticated using (is_group_member(group_id));

-- [FIX 4] insert now requires can_post_in_group() so 'admins'-only chat is
-- enforced in the database, not merely greyed-out in the client.
drop policy if exists msg_insert_own on public.group_messages;
create policy msg_insert_own on public.group_messages
  for insert to authenticated
  with check (sender_id = auth.uid() and can_post_in_group(group_id));

drop policy if exists msg_update_own_or_admin on public.group_messages;
create policy msg_update_own_or_admin on public.group_messages
  for update to authenticated
  using (sender_id = auth.uid() or is_group_admin(group_id));


-- ============================================================================
--  SECTION 4 — GROUP RPC FUNCTIONS  ⚠️ SIGNATURES ONLY — NOT REPRODUCED HERE
--  These are SECURITY DEFINER functions whose full bodies live ONLY in your
--  Supabase project (not in the app code), so this file cannot faithfully
--  recreate them. They are listed so the data model is complete. If you ever
--  need to rebuild them, ask the assistant to write them from this behaviour
--  spec — do NOT assume running this file restored them.
--
--    create_group(p_name text, p_description text) -> groups row
--        Creates a group, generates a unique invite_code, inserts the caller
--        into group_members as 'admin'/owner.
--    join_group_by_code(p_code text) -> groups row
--        Looks up a group by invite_code; inserts caller as 'member'.
--    my_group() -> groups row (+ membership)
--        Returns the caller's current group (the app supports one active group).
--    delete_group(p_group_id uuid) -> void          [owner only]
--    kick_group_member(p_group_id uuid, p_user_id uuid) -> void   [admin only]
