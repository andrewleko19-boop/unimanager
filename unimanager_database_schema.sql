  user_id       uuid primary key references auth.users(id) on delete cascade,
  name          text,
  university    text,                       -- university id (FK-by-convention to universities.id)
  absence_limit int  default 5,
  theme         text default 'dark',
  lang          text default 'en',
  avatar_url    text,
  data_blob     jsonb default '{}'::jsonb,  -- schedule, slots, absences, subjectNotes,
                                            -- gpaHistory, achievements, setupDone,
                                            -- examTimes, gradeSystem, _attSubmitted
