alter table public.universities enable row level security;

-- SELECT: official rows OR your own submissions
drop policy if exists uni_select_official_or_own on public.universities;
create policy uni_select_official_or_own on public.universities
  for select to authenticated
  using (is_official = true or created_by = auth.uid());

-- INSERT: only your own, and never self-promoted to official
drop policy if exists uni_insert_own on public.universities;
create policy uni_insert_own on public.universities
  for insert to authenticated
  with check (created_by = auth.uid() and is_official = false);

-- UPDATE: only your own non-official rows; cannot flip to official
drop policy if exists uni_update_own on public.universities;
create policy uni_update_own on public.universities
  for update to authenticated
  using (created_by = auth.uid() and is_official = false)
  with check (created_by = auth.uid() and is_official = false);

-- DELETE: only your own non-official rows
drop policy if exists uni_delete_own on public.universities;
create policy uni_delete_own on public.universities
  for delete to authenticated
  using (created_by = auth.uid() and is_official = false);

