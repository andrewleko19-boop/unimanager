-- Helper: drop-then-create so this script is re-runnable.
do $$
declare t text;
begin
  foreach t in array array['user_settings','tasks','exams','subjects','study_sessions']
  loop
    execute format('drop policy if exists %I on public.%I', t||'_own_all', t);
    execute format(
      'create policy %I on public.%I for all to authenticated
         using (user_id = auth.uid()) with check (user_id = auth.uid())',
      t||'_own_all', t);
  end loop;
end $$;

-- [FIX 3] Co-member profile visibility -----------------------------------------
-- The app's group roster (fetchGroupBundle) and chat name lookups read other
-- members' display names. The own-row policy above forbids that, so names came
-- back null for everyone but yourself. RLS is row-level, not column-level, so a
-- naive "co-members can read user_settings" policy would also leak data_blob
-- (GPA history, absences, …). Instead we expose ONLY the safe columns through a
-- SECURITY DEFINER view whose WHERE clause restricts rows to co-members + self.

-- "Does the current user share at least one group with `other`?"  SECURITY
-- DEFINER so its internal read of group_members is not subject to the caller's
-- RLS (and never recurses, same trick as is_group_member).
create or replace function public.shares_group_with(other uuid)
returns boolean language sql security definer stable set search_path = public as $$
  select exists(
    select 1
    from group_members a
    join group_members b on a.group_id = b.group_id
    where a.user_id = auth.uid()
      and b.user_id = other
  );
$$;

-- Read-only directory of public profile fields. security_invoker is OFF
-- (definer semantics) so the view bypasses user_settings RLS, but the WHERE
-- clause limits visible rows to the caller's co-members (or the caller). Only
-- user_id, name and avatar_url are projected — data_blob never leaves the row.
drop view if exists public.member_directory;
create view public.member_directory
  with (security_invoker = off) as
  select user_id, name, avatar_url
  from public.user_settings
  where user_id = auth.uid() or public.shares_group_with(user_id);

grant select on public.member_directory to authenticated;


