-- --- SECURITY DEFINER helpers (break RLS recursion) --------------------------
-- These already exist in your project. Recreated here because their bodies are
-- short and known; safe to "create or replace".
create or replace function public.is_group_member(gid uuid)
returns boolean language sql security definer stable set search_path = public as $$
  select exists(select 1 from group_members where group_id = gid and user_id = auth.uid());
$$;

create or replace function public.is_group_admin(gid uuid)
returns boolean language sql security definer stable set search_path = public as $$
  select exists(select 1 from group_members
                where group_id = gid and user_id = auth.uid() and role = 'admin');
$$;

-- [FIX 4] "May the current user post in this group right now?"  Honours
-- groups.chat_mode: everyone may post when 'all', only admins when 'admins'.
-- SECURITY DEFINER so the internal reads of groups/group_members ignore the
-- caller's RLS. Used by the message INSERT policy below so the chat-mode lock
-- is enforced at the DB layer, not just hidden in the UI.
create or replace function public.can_post_in_group(gid uuid)
returns boolean language sql security definer stable set search_path = public as $$
  select exists(
    select 1
    from groups g
    join group_members m on m.group_id = g.id
    where g.id = gid
      and m.user_id = auth.uid()
      and (g.chat_mode is distinct from 'admins' or m.role = 'admin')
  );
$$;

